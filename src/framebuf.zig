// フレームバッファグラフィクス — ピクセル描画 & ビットマップフォント

// フレームバッファ設定
var fb_addr: u32 = 0;
var fb_width: u32 = 0;
var fb_height: u32 = 0;
var fb_bpp: u32 = 0; // bits per pixel
var fb_pitch: u32 = 0; // bytes per line
var available: bool = false;

pub fn init(addr: u32, width: u32, height: u32, bpp: u32) void {
    if (addr == 0 or width == 0 or height == 0) return;
    fb_addr = addr;
    fb_width = width;
    fb_height = height;
    fb_bpp = bpp;
    fb_pitch = width * (bpp / 8);
    available = true;
}

pub fn isAvailable() bool {
    return available;
}

pub fn getWidth() u32 {
    return fb_width;
}

pub fn getHeight() u32 {
    return fb_height;
}

pub fn putPixel(x: u32, y: u32, color: u32) void {
    if (!available or x >= fb_width or y >= fb_height) return;
    const offset = y * fb_pitch + x * (fb_bpp / 8);
    if (fb_bpp == 32) {
        const ptr: *volatile u32 = @ptrFromInt(fb_addr + offset);
        ptr.* = color;
    } else if (fb_bpp == 24) {
        const base: [*]volatile u8 = @ptrFromInt(fb_addr + offset);
        base[0] = @truncate(color);
        base[1] = @truncate(color >> 8);
        base[2] = @truncate(color >> 16);
    } else if (fb_bpp == 16) {
        const ptr: *volatile u16 = @ptrFromInt(fb_addr + offset);
        ptr.* = @truncate(color);
    }
}

pub fn drawRect(x: u32, y: u32, w: u32, h: u32, color: u32) void {
    if (!available) return;
    // 上辺・下辺
    var i: u32 = 0;
    while (i < w) : (i += 1) {
        putPixel(x + i, y, color);
        if (h > 0) putPixel(x + i, y + h - 1, color);
    }
    // 左辺・右辺
    i = 0;
    while (i < h) : (i += 1) {
        putPixel(x, y + i, color);
        if (w > 0) putPixel(x + w - 1, y + i, color);
    }
}

pub fn fillRect(x: u32, y: u32, w: u32, h: u32, color: u32) void {
    if (!available) return;
    var py: u32 = 0;
    while (py < h) : (py += 1) {
        var px: u32 = 0;
        while (px < w) : (px += 1) {
            putPixel(x + px, y + py, color);
        }
    }
}

pub fn clear(color: u32) void {
    if (!available) return;
    fillRect(0, 0, fb_width, fb_height, color);
}

// ---- ビットマップフォント (8x16) ----
// 簡易版: ASCII 32-126 (95 文字)
// 各文字は 16 バイト (8 ピクセル幅 x 16 行)

pub fn drawChar(x: u32, y: u32, char: u8, color: u32) void {
    if (!available) return;
    if (char < 32 or char > 126) return;
    const idx: usize = char - 32;
    const glyph = &font_data[idx];
    var row: u32 = 0;
    while (row < 16) : (row += 1) {
        const bits = glyph[row];
        var col: u32 = 0;
        while (col < 8) : (col += 1) {
            if (bits & (@as(u8, 0x80) >> @truncate(col)) != 0) {
                putPixel(x + col, y + row, color);
            }
        }
    }
}

pub fn drawString(x: u32, y: u32, str: []const u8, color: u32) void {
    if (!available) return;
    var cx = x;
    for (str) |c| {
        if (c == '\n') {
            // 次の行には対応しない (呼び出し側で管理)
            continue;
        }
        drawChar(cx, y, c, color);
        cx += 8;
        if (cx + 8 > fb_width) break;
    }
}

