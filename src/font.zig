// Extended Font Support -- 8x8 compact bitmap font
// Full ASCII printable range (32-126) with bold and outline rendering

const framebuf = @import("framebuf.zig");

// ---- Constants ----

pub const CHAR_WIDTH: u8 = 8;
pub const CHAR_HEIGHT: u8 = 8;
const GLYPH_COUNT = 95; // ASCII 32-126

// ---- Public API ----

/// Get character cell width
pub fn getCharWidth() u8 {
    return CHAR_WIDTH;
}

/// Get character cell height
pub fn getCharHeight() u8 {
    return CHAR_HEIGHT;
}

/// Measure string width in pixels
pub fn measureString(str: []const u8) u32 {
    return @as(u32, @truncate(str.len)) * CHAR_WIDTH;
}

/// Draw a single 8x8 character
pub fn drawChar8x8(x: u32, y: u32, char: u8, color: u32) void {
    if (char < 32 or char > 126) return;
    const idx: usize = char - 32;
    const glyph = &font_8x8[idx];
    var row: u32 = 0;
    while (row < 8) : (row += 1) {
        const bits = glyph[row];
        var col: u32 = 0;
        while (col < 8) : (col += 1) {
            if (bits & (@as(u8, 0x80) >> @truncate(col)) != 0) {
                framebuf.putPixel(x + col, y + row, color);
            }
        }
    }
}

/// Draw a string using 8x8 font
pub fn drawString8x8(x: u32, y: u32, str: []const u8, color: u32) void {
    var cx = x;
    for (str) |c| {
        if (c == '\n') continue;
        drawChar8x8(cx, y, c, color);
        cx += CHAR_WIDTH;
        if (cx + CHAR_WIDTH > framebuf.getWidth()) break;
    }
}

/// Draw bold character (double-strike: draw at x and x+1)
pub fn drawCharBold(x: u32, y: u32, char: u8, color: u32) void {
    if (char < 32 or char > 126) return;
    const idx: usize = char - 32;
    const glyph = &font_8x8[idx];
    var row: u32 = 0;
    while (row < 8) : (row += 1) {
        const bits = glyph[row];
        var col: u32 = 0;
        while (col < 8) : (col += 1) {
            if (bits & (@as(u8, 0x80) >> @truncate(col)) != 0) {
                framebuf.putPixel(x + col, y + row, color);
                framebuf.putPixel(x + col + 1, y + row, color);
            }
        }
    }
}

/// Draw bold string
pub fn drawStringBold(x: u32, y: u32, str: []const u8, color: u32) void {
    var cx = x;
    for (str) |c| {
        if (c == '\n') continue;
        drawCharBold(cx, y, c, color);
        cx += CHAR_WIDTH + 1; // extra pixel for bold
        if (cx + CHAR_WIDTH + 1 > framebuf.getWidth()) break;
    }
}

/// Draw character with background (outline effect)
pub fn drawCharOutline(x: u32, y: u32, char: u8, fg: u32, bg: u32) void {
    if (char < 32 or char > 126) return;
    const idx: usize = char - 32;
    const glyph = &font_8x8[idx];
    var row: u32 = 0;
    while (row < 8) : (row += 1) {
        const bits = glyph[row];
        var col: u32 = 0;
        while (col < 8) : (col += 1) {
            if (bits & (@as(u8, 0x80) >> @truncate(col)) != 0) {
                framebuf.putPixel(x + col, y + row, fg);
            } else {
                framebuf.putPixel(x + col, y + row, bg);
            }
        }
    }
}

/// Draw string with background
pub fn drawStringOutline(x: u32, y: u32, str: []const u8, fg: u32, bg: u32) void {
    var cx = x;
    for (str) |c| {
        if (c == '\n') continue;
        drawCharOutline(cx, y, c, fg, bg);
        cx += CHAR_WIDTH;
        if (cx + CHAR_WIDTH > framebuf.getWidth()) break;
    }
}

/// Measure string width for bold rendering
pub fn measureStringBold(str: []const u8) u32 {
    if (str.len == 0) return 0;
    return @as(u32, @truncate(str.len)) * (CHAR_WIDTH + 1);
}

